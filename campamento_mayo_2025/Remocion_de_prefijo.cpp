void solve() {
    int n;
    cin >> n;
    vector<int> s(n);
    bitset<200005> b;
    for(auto &e : s)
        cin >> e;
    for(int i = n-1; i >= 0; i--) {
        if(b[s[i]]) {
            cout << i+1 << '\n';
            return;
        }
        b[s[i]].flip();
    }
    cout << 0 << '\n';
}

